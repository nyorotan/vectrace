type layerData struct {
	paths  *vectrace.Path
	color  RGB
	area   int
	bm     *vectrace.Bitmap
	isBase bool
}

type colorCluster struct {
	count            int
	sumR, sumG, sumB uint64
	rep              RGB // 代表色（平均値）
}

type RGB struct {
	R, G, B uint8
}

func (c RGB) Hex() int {
	return int(c.R)<<16 | int(c.G)<<8 | int(c.B)
}

