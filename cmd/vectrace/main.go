var fForceBlack *bool // ベースレイヤーを黒に強制するか

func getPrinter() *message.Printer {
	var prefs []string
	if lang := os.Getenv("LANG"); lang != "" {
		prefs = append(prefs, lang)
	}
	if lc := os.Getenv("LC_ALL"); lc != "" {
		prefs = append(prefs, lc)
	}
	// Windowsの場合のフォールバック
	if runtime.GOOS == "windows" {
		if winLang := getWindowsLocale(); winLang != "" {
			prefs = append(prefs, winLang)
		}
	}

	tag, _ := language.MatchStrings(language.NewMatcher([]language.Tag{
		language.English,
		language.Japanese,
	}), prefs...)
	return message.NewPrinter(tag)
}

